    private void btnAddToTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToTicketActionPerformed
        DefaultTableModel model = (DefaultTableModel)tblTicket.getModel();
        boolean yaExiste = false;
        for(int i = 0; i < model.getRowCount() && !yaExiste; i++){
            if(model.getValueAt(i, 0).equals((String)cboxProducts.getSelectedItem())){
                model.setValueAt((int) model.getValueAt(i, 1)+Integer.parseInt(txtUnits.getText()), i, 1);
                yaExiste = true;
            }
        }
        if(!yaExiste){ 
            Object[] row = new Object[]{(String)cboxProducts.getSelectedItem(), Integer.parseInt(txtUnits.getText()), Double.parseDouble(txtUnitPrice.getText())};
            model.addRow(row);
        }
        tblTicket.setModel(model);
    }//GEN-LAST:event_btnAddToTicketActionPerformed

