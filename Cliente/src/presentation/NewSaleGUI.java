    private void fillProductsCombo(){
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        Product p = new Product();

        for(Product prod : p.getDao().getProductsList())
            if(!prod.isDeleted())
                model.addElement(prod.getProductname());
        
        cboxProducts.setModel(model);
    }

